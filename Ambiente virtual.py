#                       AMBIENTE VIRTUAL

#   Criando um ambiente virtual no Windows:
# Crie uma pasta para o projeto e pip install virtualenv.
        #pip é o gerenciador de pacotes.
# virtualenv nome_da_virtualenv (ex: testezero) ou python -m virtuallenv nome_da_virtualenv.
# nome_virtual_env/scripts/activate (para a ativação do ambiente). #dar tab
# Em caso de erro abra o powershell na barra de programas e digite: Set-ExecutionPolicy -Scope CurrentUser -ExecutionPolicy Remotesigned
        #refazer o processo desde o inicio, que dará certo!!!
# Para desativar o ambiente (deactivate).
# Todas as bibliotecas instaladas ficará somente neste ambiente.

